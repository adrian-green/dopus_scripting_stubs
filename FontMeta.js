/**
 *This object provides metadata properties relating to font files. It is obtained from the Metadata object.
 */
class FontMeta {
	/**
	 * <p>The character set.</p>
	 * @returns {int}
	 */

	charset;
	/**
	 * <p>The clipping precision.</p>
	 * @returns {int}
	 */

	clipprecision;
	/**
	 * <p>The angle, in tenths of degrees, between the escapement vector and the
	 *       x-axis of the device.</p>
	 * @returns {int}
	 */

	escapement;
	/**
	 * <p>The typeface name of the font.</p>
	 * @returns {string}
	 */

	fontname;
	/**
	 * <p>The height, in logical units, of the font's character cell or
	 *       character.</p>
	 * @returns {int}
	 */

	height;
	/**
	 * <p>An italic font if set to <strong>True</strong>.</p>
	 * @returns {boolean}
	 */

	italic;
	/**
	 * <p>The angle, in tenths of degrees, between each character's base line and
	 *       the x-axis of the device.</p>
	 * @returns {int}
	 */

	orientation;
	/**
	 * <p>The output precision.</p>
	 * @returns {int}
	 */

	outprecision;
	/**
	 * <p>The pitch and family of the font.</p>
	 * @returns {int}
	 */

	pitchandfamily;
	/**
	 * <p>The output quality.</p>
	 * @returns {int}
	 */

	quality;
	/**
	 * <p>A strikeout font if set to <strong>True</strong>.</p>
	 * @returns {boolean}
	 */

	strikeout;
	/**
	 * <p>An underlined font if set to <strong>True</strong>.</p>
	 * @returns {boolean}
	 */

	underline;
	/**
	 * <p>The weight of the font in the range 0 through 1000.</p>
	 * @returns {int}
	 */

	weight;
	/**
	 * <p>The average width, in logical units, of characters in the
	 *   font.</p>
	 * @returns {int}
	 */

	width;
} //end class
