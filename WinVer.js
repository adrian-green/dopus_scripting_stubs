/**
 *This object represents information about the current Windows version. It is obtained from the Version.winver property.
 */
class WinVer {
	/**
	 * <p>Full Windows version string.</p>
	 * @returns {string}
	 */

	_default_value
	/**
	 * <p><strong>True </strong>if running on a Server edition of
	 *   Windows.</p>
	 * @returns {boolean}
	 */

	server;
	/**
	 * <p><strong>True</strong> if running on Windows XP.</p>
	 * @returns {boolean}
	 */

	xp;
	/**
	 * <p><strong>True </strong>if running on Windows XP or better (this will
	 *       always be true).</p>
	 * @returns {boolean}
	 */

	xporbetter;
	/**
	 * <p><strong>True </strong>if running on Windows Vista.</p>
	 * @returns {boolean}
	 */

	vista;
	/**
	 * <p><strong>True</strong> if running on Windows Vista or better
	 *     (later).</p>
	 * @returns {boolean}
	 */

	vistaorbetter;
	/**
	 * <p><strong>True </strong>if running on Windows 7.</p>
	 * @returns {boolean}
	 */

	win7;
	/**
	 * <p><strong>True</strong> if running on Windows 7 or better.</p>
	 * @returns {boolean}
	 */

	win7orbetter;
	/**
	 * <p><strong>True </strong>if running on Windows 8.</p>
	 * @returns {boolean}
	 */

	win8;
	/**
	 * <p><strong>True </strong>if running on Windows 8 or better.</p>
	 * @returns {boolean}
	 */

	win8orbetter;
	/**
	 * <p><strong>True </strong>if running on Windows 8.1.</p>
	 * @returns {boolean}
	 */

	win81;
	/**
	 * <p><strong>True </strong>if running on Windows 8.1 or better.</p>
	 * @returns {boolean}
	 */

	win81orbetter;
	/**
	 * <p><strong>True </strong>if running on Windows 10.</p>
	 * @returns {boolean}
	 */

	win10;
	/**
	 * <p><strong>True </strong>if running on Windows 10 or better.</p>
	 * @returns {boolean}
	 */

	win10orbetter;
	/**
	 * <p><strong>True </strong>if running on Windows 11.</p>
	 * @returns {boolean}
	 */

	win11;
	/**
	 * <p><strong>True </strong>if running on Windows 11 or
	 *   better.</p>
	 * @returns {boolean}
	 */

	win11orbetter;
} //end class
