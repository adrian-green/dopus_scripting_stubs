/**
 *This object represents an image file or icon loaded from disk that can be displayed in a script dialog.
 */
class Image {
	/**
	 * <p>Returns the bit count of the loaded image.</p>
	 * @returns {int}
	 */

	bitcount;
	/**
	 * <p>Returns the height of the loaded image.</p>
	 * @returns {int}
	 */

	height;
	/**
	 * <p>Returns the width of the loaded image.</p>
	 * @returns {int}
	 */

	width;
} //end class
